window.applyLang = function(lang) {
  if (!window.I18N[lang]) lang = 'hr';
  window.currentLang = lang;
  localStorage.setItem('lang', lang);
  document.documentElement.lang = lang;

  /* textContent elements */
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const val = window.t(el.getAttribute('data-i18n'));
    if (val !== undefined) el.textContent = val;
  });

  /* innerHTML elements */
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const val = window.t(el.getAttribute('data-i18n-html'));
    if (val !== undefined) el.innerHTML = val;
  });

  /* placeholder attributes */
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const val = window.t(el.getAttribute('data-i18n-placeholder'));
    if (val !== undefined) el.setAttribute('placeholder', val);
  });

  /* aria-label attributes */
  document.querySelectorAll('[data-i18n-aria-label]').forEach(el => {
    const val = window.t(el.getAttribute('data-i18n-aria-label'));
    if (val !== undefined) el.setAttribute('aria-label', val);
  });

  /* Update dropdown UI */
  const flagCodes = { hr:'hr', si:'si', de:'de', en:'gb' };
  const flagEl = document.getElementById('langCurrentFlag');
  if (flagEl) flagEl.className = `langSwitcher__flag fi fi-${flagCodes[lang] || 'hr'}`;
  document.querySelectorAll('.langSwitcher__option').forEach(btn => {
    btn.classList.toggle('is-active', btn.dataset.lang === lang);
  });

  /* Notify dynamic components */
  document.dispatchEvent(new CustomEvent('langChanged'));
};

